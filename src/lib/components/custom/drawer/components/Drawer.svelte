<script lang="ts">
	import { ctx } from '../ctx.js';
	import type { Props } from '../types.js';

	type $$Props = Props;

	export let preventScroll: $$Props['preventScroll'] = false;
	export let closeOnEscape: $$Props['closeOnEscape'] = false;
	export let closeOnOutsideClick: $$Props['closeOnOutsideClick'] = false;
	export let portal: $$Props['portal'] = undefined;
	export let forceVisible: $$Props['forceVisible'] = false;
	export let open: $$Props['open'] = undefined;

	const {
		states: { open: localOpen },
		updateOption
	} = ctx.set({
		preventScroll,
		closeOnEscape,
		closeOnOutsideClick,
		portal,
		forceVisible,
		defaultOpen: open,
		onOpenChange: ({ next }) => {
			open = next;
			return next;
		}
	});

	$: open !== undefined && localOpen.set(open);

	$: updateOption('preventScroll', preventScroll);
	$: updateOption('closeOnEscape', closeOnEscape);
	$: updateOption('closeOnOutsideClick', closeOnOutsideClick);
	$: updateOption('portal', portal);
	$: updateOption('forceVisible', forceVisible);
</script>

<slot />
